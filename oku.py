import random
import string

def ランダム文字列生成(length=10):
    """指定した長さのランダムな文字列を生成する関数"""
    return ''.join(random.choices(string.ascii_letters + string.digits, k=length))

def 素数判定(n):
    """指定した数値が素数かどうかを判定する関数"""
    if n < 2:
        return False
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:
            return False
    return True

def フィボナッチ(n):
    """フィボナッチ数列を生成する関数"""
    a, b = 0, 1
    for _ in range(n):
        yield a
        a, b = b, a + b

